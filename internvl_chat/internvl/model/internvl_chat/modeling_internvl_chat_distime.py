    @torch.no_grad()
    def generate(
            self,
            pixel_values: Optional[torch.FloatTensor] = None,
            input_ids: Optional[torch.FloatTensor] = None,
            attention_mask: Optional[torch.LongTensor] = None,
            visual_features: Optional[torch.FloatTensor] = None,
            generation_config: Optional[GenerationConfig] = None,
            output_hidden_states: Optional[bool] = None,
            **generate_kwargs,
    ) -> torch.LongTensor:
        
        assert self.img_context_token_id is not None
        if pixel_values is not None:
            if visual_features is not None:
                vit_embeds = visual_features
            else:
                vit_embeds = self.extract_feature(pixel_values)
            input_embeds = self.language_model.get_input_embeddings()(input_ids)
            B, N, C = input_embeds.shape
            input_embeds = input_embeds.reshape(B * N, C)

            input_ids = input_ids.reshape(B * N)
            selected = (input_ids == self.img_context_token_id)
            assert selected.sum() != 0
            input_embeds[selected] = vit_embeds.reshape(-1, C).to(input_embeds.device)

            input_embeds = input_embeds.reshape(B, N, C)
        else:
            input_embeds = self.language_model.get_input_embeddings()(input_ids)

        outputs = self.language_model.generate(
            inputs_embeds=input_embeds,
            attention_mask=attention_mask,
            generation_config=generation_config,
            output_hidden_states=output_hidden_states,
            use_cache=True,
            **generate_kwargs,
        )

        return outputs


    @torch.no_grad()
    def generate_distime(
            self,
            pixel_values: Optional[torch.FloatTensor] = None,
            input_ids: Optional[torch.FloatTensor] = None,
            attention_mask: Optional[torch.LongTensor] = None,
            visual_features: Optional[torch.FloatTensor] = None,
            generation_config: Optional[GenerationConfig] = None,
            frame_times: Optional[torch.LongTensor] = None,
            duration: Optional[int] = None,
            id: Optional[str] = None,
            output_hidden_states: Optional[bool] = True,
            **generate_kwargs,
    ) -> torch.LongTensor:
        assert self.img_context_token_id is not None
        if pixel_values is not None:
            if visual_features is not None:
                vit_embeds = visual_features
            else:
                vit_embeds = self.extract_feature(pixel_values)
            input_embeds = self.language_model.get_input_embeddings()(input_ids)
            B, N, C = input_embeds.shape
            input_embeds = input_embeds.reshape(B * N, C)

            input_ids = input_ids.reshape(B * N)
            selected = (input_ids == self.img_context_token_id)
            assert selected.sum() != 0
            input_embeds[selected] = vit_embeds.reshape(-1, C).to(input_embeds.device)

            duration_enc = torch.tensor(duration, device=input_ids.device)
            selected_frame_time = (input_ids == self.frame_time_context_token_id)
            
            frame_times = torch.tensor(frame_times, device=input_ids.device)
            
            frame_times_norm = frame_times / duration_enc * self.reg_max

            gaussian_dists_frame_times = self.generate_gaussian_peaks(self.reg_max + 1, frame_times_norm, sigma=self.sigma)
            gaussian_dists_frame_times = gaussian_dists_frame_times.reshape(-1, 2 * (self.reg_max + 1))

            frame_times_embeddings = self.time_enc(gaussian_dists_frame_times)
         
            input_embeds[selected_frame_time] = input_embeds[selected_frame_time] * 0.0 + frame_times_embeddings
           
            input_embeds = input_embeds.reshape(B, N, C)
        else:
            input_embeds = self.language_model.get_input_embeddings()(input_ids)

        llm_outputs = self.language_model.generate(
            inputs_embeds=input_embeds,
            attention_mask=attention_mask,
            generation_config=generation_config,
            output_hidden_states=True,
            return_dict_in_generate=True,
            use_cache=True,
            duration=None,
            id=None,
            **generate_kwargs,
        )
    
        llm_outputs_ids = llm_outputs.sequences
        pred_time = llm_outputs.pred_time

        return llm_outputs_ids, pred_time


